import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import { basename, join } from 'node:path';

import { Marked, Renderer } from 'marked';
import { format, resolveConfig } from 'prettier';

const workspaceRoot = new URL('../', import.meta.url);
const docsInputDirectory = new URL('apps/showcase/public/docs/', workspaceRoot);
const docsOutputFile = new URL('apps/showcase/src/app/pages/docs/docs-html-registry.ts', workspaceRoot);

const slugifyMarkdownHeading = (value) =>
  value
    .toLowerCase()
    .trim()
    .replace(/[`'"]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

const getUniqueMarkdownHeadingId = (baseId, usedIds) => {
  let id = baseId;
  let index = 2;

  while (usedIds.has(id)) {
    id = `${baseId}-${index}`;
    index += 1;
  }

  return id;
};

const createRenderer = () => {
  const renderer = new Renderer();
  const headings = [];
  const usedHeadingIds = new Set();

  renderer.heading = function heading({ tokens, depth, text }) {
    const headingHtml = this.parser.parseInline(tokens);
    const baseId = slugifyMarkdownHeading(text);

    if (!baseId) {
      return `<h${depth}>${headingHtml}</h${depth}>\n`;
    }

    const id = getUniqueMarkdownHeadingId(baseId, usedHeadingIds);

    usedHeadingIds.add(id);
    headings.push({ depth, id });

    return `<h${depth}>${headingHtml}</h${depth}>\n`;
  };

  return { renderer, headings };
};

const renderMarkdown = (markdown) => {
  const { renderer, headings } = createRenderer();
  const html = new Marked({
    async: false,
    breaks: false,
    gfm: true,
    pedantic: false,
    renderer
  }).parse(markdown, { async: false });

  return { html, headings };
};

const files = (await readdir(docsInputDirectory))
  .filter((file) => file.endsWith('.md'))
  .sort((first, second) => first.localeCompare(second));

const entries = await Promise.all(
  files.map(async (file) => {
    const markdown = await readFile(join(docsInputDirectory.pathname, file), 'utf8');

    return [`/docs/${basename(file)}`, renderMarkdown(markdown)];
  })
);

const prettierOptions = (await resolveConfig(docsOutputFile.pathname)) ?? {};
const output = await format(
  `/* eslint-disable max-lines -- generated docs HTML registry */
/* This file is generated by tools/generate-showcase-docs.mjs. */

export type DocsHtmlHeading = {
  readonly depth: number;
  readonly id: string;
};

export type DocsHtmlEntry = {
  readonly html: string;
  readonly headings: readonly DocsHtmlHeading[];
};

export const docsHtmlRegistry = ${JSON.stringify(Object.fromEntries(entries), null, 2)} as const satisfies Readonly<
  Record<string, DocsHtmlEntry>
>;

export function findDocsHtmlEntry(markdownPath: string): DocsHtmlEntry | undefined {
  return (docsHtmlRegistry as Readonly<Record<string, DocsHtmlEntry>>)[markdownPath];
}
`,
  { ...prettierOptions, filepath: docsOutputFile.pathname, parser: 'typescript' }
);

await mkdir(new URL('./', docsOutputFile), { recursive: true });
await writeFile(docsOutputFile, output);
